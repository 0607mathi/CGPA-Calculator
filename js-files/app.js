	let subgrade4=Number(document.getElementById('subgrade4').value);
	let subcredits4=Number(document.getElementById('subcredits4').textContent);
	let subgrade5=Number(document.getElementById('subgrade5').value);
	let subcredits5=Number(document.getElementById('subcredits5').textContent);
